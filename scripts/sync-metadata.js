const fs = require('fs')
const path = require('path')

const POSTS_DIR = path.join(__dirname, '..', 'content', 'posts')
const META_FILE = path.join(__dirname, '..', 'lib', 'posts-meta.js')
const PAGE_FILE = path.join(__dirname, '..', 'app', 'blog', '[slug]', 'page.js')

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/)
  if (!match) return null
  const lines = match[1].split('\n')
  const fm = {}
  for (const line of lines) {
    const m = line.match(/^([a-zA-Z0-9_]+):\s*(.*)$/)
    if (m) {
      let val = m[2].trim()
      if (val.startsWith('"') && val.endsWith('"')) val = val.slice(1, -1)
      fm[m[1]] = val
    }
  }
  return fm
}

function extractFirstImage(content, fmImage) {
  // 1. Check if frontmatter has an image field
  if (fmImage) {
    let url = fmImage
    // If it's a photo ID (not full URL), build the Unsplash URL
    if (fmImage.startsWith('photo-')) {
      url = `https://images.unsplash.com/${fmImage}?w=600&h=400&fit=crop`
      return url
    }
    if (fmImage.startsWith('https://images.unsplash.com/')) {
      // Add dimensions if missing (for card display)
      if (!url.includes('w=')) {
        url += (url.includes('?') ? '&' : '?') + 'w=600&h=400&fit=crop'
      }
      return url
    }
  }

  // 2. Extract first Unsplash image from markdown content
  const imgMatch = content.match(/!\[.*?\]\((https:\/\/images\.unsplash\.com\/[^)]+)\)/)
  if (imgMatch) {
    let url = imgMatch[1]
    // Add dimensions if missing (for card display)
    if (!url.includes('w=')) {
      url += (url.includes('?') ? '&' : '?') + 'w=600&h=400&fit=crop'
    }
    return url
  }

  // 3. Fallback to a default image
  return 'https://images.unsplash.com/photo-1677442136019-21780ecad995?w=600&h=400&fit=crop'
}

function normalizeDate(dateStr) {
  // If already ISO format (YYYY-MM-DD), return as-is
  if (/^\d{4}-\d{2}-\d{2}$/.test(dateStr)) return dateStr
  
  // Extract YYYY-MM-DD from ISO datetime with timezone (e.g. 2026-06-30T04:20:00+08:00)
  const isoDateMatch = dateStr.match(/^(\d{4}-\d{2}-\d{2})T/)
  if (isoDateMatch) return isoDateMatch[1]

  // Parse "Month DD, YYYY" format manually (no timezone issues)
  const monthMap = {
    'January': '01', 'February': '02', 'March': '03', 'April': '04',
    'May': '05', 'June': '06', 'July': '07', 'August': '08',
    'September': '09', 'October': '10', 'November': '11', 'December': '12'
  }
  const m = dateStr.match(/([A-Za-z]+)\s+(\d{1,2}),?\s+(\d{4})/)
  if (m && monthMap[m[1]]) {
    const month = monthMap[m[1]]
    const day = m[2].padStart(2, '0')
    const year = m[3]
    return `${year}-${month}-${day}`
  }
  // Fallback: try Date parsing
  const d = new Date(dateStr)
  if (!isNaN(d.getTime())) {
    const year = d.getFullYear()
    const month = String(d.getMonth() + 1).padStart(2, '0')
    const day = String(d.getDate()).padStart(2, '0')
    return `${year}-${month}-${day}`
  }
  return dateStr
}

// Extract excerpt from article content when frontmatter lacks it
function extractExcerpt(content, maxLength = 200) {
  // Remove frontmatter
  const body = content.replace(/^---[\s\S]*?---/, '').trim()
  
  // Find first paragraph that's not a heading, image, table, or HTML
  const paragraphs = body.split('\n\n')
  for (const para of paragraphs) {
    const trimmed = para.trim()
    // Skip headings, images, tables, HTML tags, captions
    if (trimmed.startsWith('#') || trimmed.startsWith('![') || 
        trimmed.startsWith('|') || trimmed.startsWith('<') ||
        trimmed.startsWith('*') && trimmed.endsWith('*') ||
        trimmed.startsWith('[') || trimmed.length < 50) {
      continue
    }
    // Clean markdown formatting
    let clean = trimmed
      .replace(/\*\*(.*?)\*\*/g, '$1')  // bold
      .replace(/\*(.*?)\*/g, '$1')      // italic
      .replace(/\[(.*?)\]\(.*?\)/g, '$1') // links
      .replace(/`(.*?)`/g, '$1')         // code
      .replace(/#{1,6}\s+/g, '')         // headings
      .trim()
    
    if (clean.length >= 50) {
      if (clean.length > maxLength) {
        clean = clean.substring(0, maxLength).replace(/\s+\S*$/, '') + '...'
      }
      return clean
    }
  }
  return ''
}

function generatePostsMeta(posts) {
  const entries = posts.map(p => {
    const slug = p.slug || path.basename(p.file, '.md')
    const title = p.title || slug
    const category = p.category || 'AI'
    const excerpt = p.excerpt || ''
    const date = normalizeDate(p.date || '2026-01-01')
    const readTime = p.readTime || '16 min read'
    const image = p.image || ''
    return `  {
    slug: '${slug}',
    title: ${JSON.stringify(title)},
    category: ${JSON.stringify(category)},
    excerpt: ${JSON.stringify(excerpt)},
    date: '${date}',
    readTime: '${readTime}',
    image: ${JSON.stringify(image)}
  }`
  }).join(',\n')

  return `// AI in China - Posts Metadata
// 文章元数据管理文件
// ⚠️ AUTO-GENERATED by scripts/sync-metadata.js — DO NOT EDIT MANUALLY
// Generated: ${new Date().toISOString()}

const allPosts = [
${entries}
]

export { allPosts }
`
}

function generatePostMetadata(posts) {
  const entries = posts.map(p => {
    const slug = p.slug || path.basename(p.file, '.md')
    const metaTitle = p.metaTitle || p.title || slug
    const metaDescription = p.metaDescription || p.excerpt || ''
    const keywords = p.keywords || ''
    return `  '${slug}': {
    metaTitle: ${JSON.stringify(metaTitle)},
    metaDescription: ${JSON.stringify(metaDescription)},
    keywords: '${keywords}',
  }`
  }).join(',\n')

  return `const postMetadata = {
${entries}
}`
}

function main() {
  // 1. Scan all markdown files
  const files = fs.readdirSync(POSTS_DIR)
    .filter(f => f.endsWith('.md'))
    .map(f => path.join(POSTS_DIR, f))

  let posts = []
  for (const file of files) {
    const content = fs.readFileSync(file, 'utf8')
    const fm = parseFrontmatter(content)
    if (fm) {
      fm.file = file
      // Extract first image from article content for card display
      // Prefer frontmatter image field, then inline markdown image, then fallback
      fm.image = extractFirstImage(content, fm.heroImage || fm.image)
      // Auto-generate excerpt if missing
      if (!fm.excerpt || fm.excerpt.trim() === '') {
        fm.excerpt = extractExcerpt(content)
      }
      posts.push(fm)
    }
  }

  // 2. Sort by date descending (newest first)
  posts.sort((a, b) => new Date(b.date) - new Date(a.date))

  // 3. Write lib/posts-meta.js
  const metaContent = generatePostsMeta(posts)
  fs.writeFileSync(META_FILE, metaContent)
  console.log(`✅ Updated ${path.relative(process.cwd(), META_FILE)} (${posts.length} posts)`)

  // 4. Patch app/blog/[slug]/page.js (replace postMetadata block)
  let pageContent = fs.readFileSync(PAGE_FILE, 'utf8')
  const newMetadataBlock = generatePostMetadata(posts)

  // Replace the existing postMetadata = { ... } block
  const metadataRegex = /const postMetadata = \{[\s\S]*?\n\}/
  if (metadataRegex.test(pageContent)) {
    pageContent = pageContent.replace(metadataRegex, newMetadataBlock)
    fs.writeFileSync(PAGE_FILE, pageContent)
    console.log(`✅ Updated ${path.relative(process.cwd(), PAGE_FILE)}`)
  } else {
    console.error(`❌ Could not find postMetadata block in page.js — manual fix needed`)
    process.exit(1)
  }

  console.log('\n🎉 Metadata sync complete. Run npm run build to deploy.')
}

main()
